public class Demo14 {
    public static void main(String[] args) {
        /**
         * while输出1-100所以数据
         * for输出1-100所以数据
         */
        for (int i = 1; i <=100 ; i++) {
            System.out.println(i);
        }
        int i = 1;
        while (i<=100){
            System.out.println(i);
            i++;
        }
    }
}
